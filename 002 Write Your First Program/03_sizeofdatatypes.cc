#include<bits/stdc++.h>
using namespace std;

int main () {
    int a = 123;
    float b = 12.4;
    bool c = false;
    double d = 12.68;
    char e = '*';
    cout<<"Size Of a is "<<sizeof(a)<<endl;
    cout<<"Size Of b is "<<sizeof(b)<<endl;
    cout<<"Size Of c is "<<sizeof(c)<<endl;
    cout<<"Size Of d is "<<sizeof(d)<<endl;
    cout<<"Size Of e is "<<sizeof(e)<<endl;
    return 0;
}